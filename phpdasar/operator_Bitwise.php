<?php
$a = 60;
$b = 13;

//bitwise AND
$c = $a & $b;
echo"$a & $b = $c";
echo"<hr>";

//bitwise OR
$c = $a | $b;
echo"$a | $b = $c";
echo"<hr>";

//bitwise XOR 
$c = $a ^ $b;
echo"$a ^ $b = $c";
echo"<hr>";

//Shift left
$c = $a << $b;
echo "$a << $b = $c";
echo "<hr>";

//Shift right
$c = $a >> $b;
echo"$a >> $b = $c";
echo"<hr>";
?>