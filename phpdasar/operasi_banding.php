<?php

$a = 5;
$b = '5';

//Sama dengan (==)
var_dump($a == $b);
echo "<hr/>";

//identik (===)
var_dump($a === $b);
echo "<hr/>";

//TIdak sama dengan (!= atau <>)
var_dump($a != $b);
echo "<hr/>";

//Tidak indentik (!==)
var_dump($a !== $b);
echo"<hr/>;";

//lebih besar dari (>)
var_dump($a > $b);
echo"<hr/>";

//Lebih kecil dari (<)
var_dump($a < $b);
echo"<hr/>";

//lebih besar dari atau sama dengan (>=)
var_dump($a >= $b);
echo"<hr/>";

//Lebih kecil dari atau sama dengan (<=)
var_dump($a <= $b);
echo"<hr/>";

?>